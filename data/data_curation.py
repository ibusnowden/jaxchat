"""
Why Deduplication Matters
Problem: Web crawl data (CommonCrawl) contains massive duplication
  - Same article scraped from 1000 mirror sites
  - Boilerplate text (cookie notices, navigation menus)
  - Near-duplicate versions of same document

Effects of training on duplicate data:
  - Model memorizes exact text instead of generalizing
  - Inflated benchmark scores (test set contamination)
  - Wasted compute training on redundant information

Scale: CommonCrawl → after dedup, 30-50% of data often removed
"""

"""### Detailed Explanation: Model-based Quality Scoring

In modern LLM pretraining (like Llama 3 or Gopher), simple heuristics (length, symbol ratio) aren't enough. We use **Model-based Scoring**:

1.  **The Oracle**: We define a 'gold standard' dataset (e.g., Wikipedia, StackOverflow, select textbooks).
2.  **The Classifier**: We train a small, fast model (like fastText or a DistilBERT) to distinguish 'Gold' data from 'Raw' web crawl data.
3.  **Inference**: We run this model over the entire TB-scale dataset. Every document gets a score from 0.0 to 1.0.
4.  **Thresholding**: We only keep documents with a score $> T$. This drastically increases the 'knowledge density' of the pretraining data, allowing smaller models to outperform larger ones trained on 'noisy' data.
"""

