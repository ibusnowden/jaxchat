class DynamicMixtureSampler:
    def __init__(self, datasets, target_ratios):
        """
        datasets: Dict of {name: list_of_docs}
        target_ratios: Dict of {name: probability}
        """
        self.datasets = datasets
        self.names = list(target_ratios.keys())
        self.weights = list(target_ratios.values())

    def sample(self, total_tokens_needed):
        mixture = []
        # In a real pipeline (SeqIO), this is deterministic and resumable
        while len(mixture) < total_tokens_needed:
            # Pick a dataset based on weights
            source = random.choices(self.names, weights=self.weights)[0]
            doc = random.choice(self.datasets[source])
            mixture.append(doc)
        return mixture

