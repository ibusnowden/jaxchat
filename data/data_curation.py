import random

class CurriculumSampler:
    def __init__(self, scored_docs):
        self.docs = scored_docs
        # Group docs by score
        self.buckets = {i: [d['text'] for d in scored_docs if d['score'] == i] for i in range(6)}

    def sample_batch(self, size, step, total_steps, warmup_frac=0.2):
        warmup_steps = total_steps * warmup_frac

        if step < warmup_steps:
            # Curriculum phase: Favor higher scores (4-5)
            high_quality_pool = self.buckets[4] + self.buckets[5]
            if not high_quality_pool:
                high_quality_pool = [d['text'] for d in self.docs]
            return random.choices(high_quality_pool, k=size)
        else:
            # Baseline phase: Random sampling from everything
            return random.choices([d['text'] for d in self.docs], k=size)

