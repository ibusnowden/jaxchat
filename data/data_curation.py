def get_duplicates_scalable(docs, dedup, n_bands=20):
    print("Computing signatures...")
    signatures = np.array([dedup.compute_minhash(d) for d in docs])
    print("Bucketing documents...")
    buckets = dedup.build_lsh_buckets(signatures, n_bands=n_bands)
    potential_duplicates = set()
    for doc_ids in buckets.values():
        if len(doc_ids) > 1:
            for i, j in combinations(sorted(doc_ids), 2):
                potential_duplicates.add((i, j))
    print(f"Verifying {len(potential_duplicates)} candidate pairs...")
    real_duplicates = [(i, j) for i, j in potential_duplicates if dedup.jaccard_from_signatures(signatures[i], signatures[j]) >= dedup.threshold]
    return real_duplicates

